int			rlink_addnew(t_ant *a, int n)
{
	if ((a->p->next = (t_rlink *)malloc(sizeof(t_rlink))) == NULL)
		return (-1);
	a->p = a->p->next;
	a->p->n = n;
	a->rlen += 1;
	return (1);
}
