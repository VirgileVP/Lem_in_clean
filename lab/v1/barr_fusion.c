#include "lem_in.h"

int				barr_fusion(t_barr *dst, t_barr *src)
{
	size_t			n;

	n = 0;
	while (n < dst->xlen && n < src->xlen)
	{
		dst->t[n] |= src->t[n];
		n += 1;
	}
	return (1);
}
