#include "colony.h"

int				colony_free(t_colony **c)
{
	if ((*c) != NULL)
	{
		while ((*c)->len > 0)
			ant_kill(*c);
		free(*c);
		*c = NULL;
		return (1);
	}
	return (0);
}
