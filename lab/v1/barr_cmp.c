#include "lem_in.h"

int				barr_cmp(t_barr *b1, t_barr *b2)
{
	size_t			n;

	n = 0;
	while (n < b1->xlen && n < b2->xlen)
	{
		if ((b1->t[n] & b2->t[n]) != 0)
			return (-1);
		n += 1;
	}
	return (1);
}
